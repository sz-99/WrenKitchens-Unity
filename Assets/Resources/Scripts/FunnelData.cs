public class FunnelData
{
    public float TopDiameter;
    public float BottomDiameter;
    public float SlopingHeight;
    public float TubeHeight;

    public FunnelData(float td, float bd, float sh, float th)
    {
        TopDiameter = td;
        BottomDiameter = bd;
        SlopingHeight = sh;
        TubeHeight = th;
    }
}
